PATCH2	EQU	$	; PATCH OUTI OPCODE
	PUSH	H
PATCH3	EQU	$
	DCX	H
	MOV	A,M
	CPI	00EH	; LOOK FOR LD C,<N>
	JNZ	PATCH3
	INX	H	; ADVANCE TO <N>
	CALL	PATCH1
	POP	H
	RET
ENDIF
* GAME IS SET UP TO RUN AT 8000H INSTEAD OF 4000H SO COPY
* 16K ROM BYTES TO 8000H
COPYTO8 EQU	$
	LXI	H,4000H
	LXI	D,8000H
	LXI	B,4000H
COPYTOL EQU	$
	MOV	A,M
	INX	H
	STAX	D
	INX	D
	DCX	B
	MOV	A,C
	ORA	B
	JNZ	COPYTOL
	RET
* CANNOT RELY ON BDOS ANYMORE AS IT WILL BE DESTROYED
* WHEN MSX BIOS IS LOADED SO WE READ THE 16550 CONSOLE
* PORT DIRECTLY
IN350R	EQU	$	; CHAR READY?
	IN	UART+5
	ANI	01H
	RET
IN350	EQU	$	; WAIT FOR INPUT
	CALL	IN350R
	JZ	IN350
	IN	UART
	RET
*
OUT350	EQU	$
	PUSH	PSW
OUT350L EQU	$
	IN	UART+5
	ANI	20H
	JZ	OUT350L
	POP	PSW
	OUT	UART
	RET
*
CRLF	EQU	$
	MVI	A,0DH
	CALL	OUT350
	MVI	A,0AH
	CALL	OUT350
	RET
IF	0
* DUMP MEM AT HL C=NUM BYTES
OUTMEM	EQU	$
	CALL	CRLF
	MOV	A,H
	CALL	OUTHEX
	MOV	A,L
	CALL	OUTHEX
	MOV	A,M
	CALL	OUTHEX
	INX	H
	DCR	C
	JNZ	OUTMEM
	CALL	CRLF
	CALL	CRLF
	RET
